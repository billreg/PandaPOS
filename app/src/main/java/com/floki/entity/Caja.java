package com.floki.entity;

public class Caja {
    private Float openingMoney;
    private Float totalVentas;
    private Float closeMoney;

    public Float getOpeningMoney() {
        return openingMoney;
    }

    public void setOpeningMoney(Float openingMoney) {
        this.openingMoney = openingMoney;
    }

    public Float getTotalVentas() {
        return totalVentas;
    }

    public void setTotalVentas(Float totalVentas) {
        this.totalVentas = totalVentas;
    }

    public Float getCloseMoney() {
        return closeMoney;
    }

    public void setCloseMoney(Float closeMoney) {
        this.closeMoney = closeMoney;
    }
}
